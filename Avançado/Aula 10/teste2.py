# Programa que insere nomes numa lista do python
# •	Método 1: Insere ao final da lista --------- append()
# •	Método 2: Insere numa posição específica------insert()


lista = [1, 2, 3, 4, 5, 6, 7, 8, 9]

print("A lista crua" , lista)

lista.append(10) #insere ao final da lista

print ('A lista adicionado 10 ao final',lista)

lista.insert(0, 'Naiara') #insere o nome no indice zero

print ('Adicionado o nome no inicio',lista)
